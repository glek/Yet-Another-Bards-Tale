package yetanotherbardtale.graphics;

/**
 * A point with double precision.
 * @author Michael Damian Mulligan (G'lek)
 * @version glek 0.0.1
 */
public class DoublePoint {
    
    public final double x, y;
    
    public DoublePoint(double x, double y) {
        this.x = x;
        this.y = y;
    }
    
    public double getX() {
        return x;
    }
    
    public double getY() {
        return y;
    }
}
